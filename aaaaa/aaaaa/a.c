#include <stdio.h>							//编译预处理命令
int main(){									//定义主函数
	int a, Sn = 0, i, n;					//定义整形变量a,Sn,i,n,并对Sn进行初始化
	printf("请输入一个一位数字:\n");		//提示输入
	scanf("%d", &a);						//输入a的值
	n = a;									//将a的值赋给n
	for (i = 0; i < 5; i++){				//一共有5个数进行相加，循环5次
		Sn = Sn + a;						//求和
		a = a * 10 + n;						//分别求出5个数，例如第一次循环为2*10+2=22 ,第二次为22*10+2...
	}
	printf("%d\n", Sn);						//输出最终结果
	return 0;								//函数返回值为0
}